#include <unistd.h>     // POSIX-функции: fork(), execlp(), getpid(), getppid(), sleep()
#include <stdio.h>      // Стандартный ввод/вывод: printf(), perror()
#include <locale.h>     // Работа с локалью (в данном коде напрямую не используется)
#include <sys/wait.h>   // waitpid(), макросы WIFEXITED(), WEXITSTATUS()
#include <stdlib.h>     // exit(), setenv()

int main() {

    // Вывод идентификатора текущего процесса (PID)
    // PID — уникальный номер процесса в системе
    printf("ID процесса второй программы: %d\n", getpid());

    // Вывод идентификатора родительского процесса (PPID)
    // Позволяет определить, кем был создан данный процесс
    printf("ID родительского процесса второй программы: %d\n", getppid());

    // Возвращаемое значение:
    // -1  → ошибка создания процесса
    //  0  → выполняется в дочернем процессе
    // >0  → выполняется в родительском процессе (значение — PID ребёнка)
    pid_t pid = fork();

    // Обработка ошибки создания дочернего процесса
    if (pid == -1) 
    {
        perror("Ошибка создания дочернего процесса");
        return 1;
    }

    // Этот блок выполняется ТОЛЬКО в дочернем процессе
    else if (pid == 0) 
    {

        // Устанавливаем переменную окружения MY_VAR
        // Параметры:
        // 1 — имя переменной
        // 2 — значение
        // 3 — флаг перезаписи (1 = перезаписывать, если уже существует)
        if (setenv("MY_VAR", "Письмо_от_родителя!", 1) == -1) {
            perror("Ошибка установки переменной окружения");
            exit(1);
        }

        execlp("./lab4_1",
               "lab4_1",   // argv[0]
               "16",
               "11",
               "2004",
               "26",
               "02",
               NULL);

        perror("Ошибка исполнения дочерней программы");
        exit(1);
    }

    // Этот блок выполняется ТОЛЬКО в родительском процессе
    else {

        // Вывод PID созданного дочернего процесса
        printf("ID дочернего процесса второй программы: %d\n", pid);

        int status;   // сюда будет записан статус завершения дочернего процесса
        pid_t w;      // переменная для хранения результата waitpid()

        // waitpid() ожидает завершения конкретного процесса (pid)
        while ((w = waitpid(pid, &status, WNOHANG)) == 0) {

            // Если ребёнок ещё не завершился — выводим сообщение
            printf("Ждем дочерний процесс\n");

            sleep(1);
        }

        if (w == -1) {
            perror("Ошибка waitpid");
        }
        else {

            // Проверяем, завершился ли процесс корректно
            // (а не был убит сигналом)
            if (WIFEXITED(status)) {

                // Извлекаем код завершения,
                // переданный через exit() в дочернем процессе
                printf("Дочерний процесс завершился с кодом %d\n",
                       WEXITSTATUS(status));
            }
        }
    }


    return 0;
}