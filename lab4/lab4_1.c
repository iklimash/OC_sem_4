#include <unistd.h>   // Объявления системных вызовов POSIX: getpid(), getppid(), sleep()
#include <stdio.h>    // Стандартный ввод/вывод: printf()
#include <stdlib.h>   // Функция exit()
#include <locale.h>   // Работа с локалью (в данном коде напрямую не используется)


int main(int argc, char* argv[], char *envp[]) {

    // getpid() возвращает PID (Process ID) текущего процесса
    printf("ID процесса первой программы: %d\r\n", getpid());

    // getppid() возвращает PID родительского процесса
    printf("ID родительского процесса первой программы: %d\r\n", getppid());

    // Сообщение о начале вывода аргументов командной строки
    printf("Аргументы, переданные в первую программу:\r\n");


    // argv[0] — имя программы
    // argv[1] и далее — переданные аргументы
    for (int i = 0; i < argc; ++i) {

        // Вывод номера аргумента (нумерация сделана с 1 для удобства)
        // и его текстового значения
        printf("Аргумент %d: %s\r\n", i + 1, argv[i]);

        sleep(1);
    }

    // Проверка: существует ли хотя бы одна переменная окружения
    // envp представляет собой массив строк, заканчивающийся NULL
    if (envp[0] != NULL) {

        printf("Получены переменные окружения:\n");

        // Перебор массива envp до тех пор,
        // пока не встретится указатель NULL (конец массива)
        for (int i = 0; envp[i] != NULL; i++) {

            printf("%s ", envp[i]);
        }
    }

    exit(13);
}